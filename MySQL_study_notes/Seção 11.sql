=============================== IFNULL ===================================
/* IFNULL(COLUNM_NAME, "TEXTO QUE DEVE APARECER PARA VALORES NULOS") */

SELECT  C.NOME, 
		IFNULL(C.EMAIL,'NAO TEM EMAIL') AS 'E-MAIL', 
		E.ESTADO, 
		T.NUMERO
FROM CLIENTES C
INNER JOIN ENDERECO E
	ON C.IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T
	ON C.IDCLIENTE = T.ID_CLIENTE;

