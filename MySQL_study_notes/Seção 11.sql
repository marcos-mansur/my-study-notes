		E.CIDADE, 
		E.ESTADO
FROM CLIENTES C 
INNER JOIN TELEFONE T 
	ON C.IDCLIENTES = T.ID_CLIENTES
INNER JOIN ENDERECO E 
	ON C.IDCLIENTES = E.ID_CLIENTES;

